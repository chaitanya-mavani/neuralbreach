/**
 * Call the Groq API to generate novel adversarial prompts based on a running
 * defence analysis.
 *
 * If the model refuses, the function returns `{ refused: true, ... }` so the
 * caller can seamlessly fall back to a local mutation engine.
 *
 * @param apiKey          - Groq API key.
 * @param defenseAnalysis - Current defence metrics from the fuzzing session.
 * @param count           - Number of new attack prompts to request.
 * @returns An {@link AIAgentResult} containing the attacks or a refusal flag.
 *
 * @example
 * ```ts
 * const result = await generateAdversarialPrompts(
 *   process.env.GROQ_API_KEY!,
 *   { totalAttempts: 50, blocked: 40, bypassed: 10, blockedCategories: ['DAN'], partialSuccesses: [] },
 *   5,
 * );
 * if (result.refused) {
 *   console.log('Agent refused, using mutation engine:', result.fallbackReason);
 * } else {
 *   result.attacks.forEach((a) => console.log(a.prompt));
 * }
 * ```
 */
export async function generateAdversarialPrompts(
  apiKey: string,
  defenseAnalysis: DefenseAnalysis,
  count: number,
): Promise<AIAgentResult> {
  // Guard: missing API key
  if (!apiKey) {
    return {
      refused: true,
      fallbackReason: 'No Groq API key provided.',
      attacks: [],
    };
  }

  try {
    const response = await fetch(GROQ_API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        model: GROQ_MODEL,
        messages: [
          { role: 'system', content: buildSystemPrompt(count) },
          { role: 'user', content: buildUserPrompt(defenseAnalysis) },
        ],
        temperature: 0.9,
        max_tokens: 4096,
      }),
    });

    // Handle HTTP-level errors
    if (!response.ok) {
      const errorBody = await response.text();
      return {
        refused: true,
        fallbackReason: `Groq API returned HTTP ${response.status}: ${errorBody}`,
        attacks: [],
      };
    }

    const data = await response.json();
    const messageContent: string =
      data?.choices?.[0]?.message?.content ?? '';

    // Check for model-level refusal
    if (isRefusal(messageContent)) {
      return {
        refused: true,
        fallbackReason: `Groq model refused to generate adversarial prompts. Response snippet: "${messageContent.slice(0, 200)}"`,
        attacks: [],
      };
    }

    // Parse the structured attacks from the model's response
    const attacks = parseAttacks(messageContent);

    if (attacks.length === 0) {
      return {
        refused: true,
        fallbackReason: `Could not parse any valid attacks from model response. Raw: "${messageContent.slice(0, 300)}"`,
        attacks: [],
      };
    }

    return { refused: false, attacks };
  } catch (err) {
    const message =
      err instanceof Error ? err.message : 'Unknown error contacting Groq API';
    return {
      refused: true,
      fallbackReason: `Network or runtime error: ${message}`,
      attacks: [],
    };
  }
}
